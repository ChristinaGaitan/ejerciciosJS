/* Tipos de datos */

// 1. Cadenas
const nombre = 'María';
console.log('Nombre: ', nombre);

// No utilice console porque que fuera una alerta
let nombreUsuario = prompt('¿Cómo te llamas?');
alert('Nombre de usuario: ' + nombreUsuario);

// 2. Números
const edad = 20;
console.log('Edad: ', edad);

let edadUsuario = prompt('¿Cuántos años tienes?');
alert('Edad de usuario: ' + edadUsuario);

// 3. Booleanos
const esEstudiante = false;
console.log('Estudiante: ', esEstudiante);

let usuarioEsEstudiante = confirm('¿Eres estudiante?');
alert('Usuario estudiante: ' + usuarioEsEstudiante);